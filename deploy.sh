# Function to validate docker-compose configuration
validate_compose() {
    log "INFO" "${GEAR} Validation de la configuration Docker Compose..."
    
    if docker-compose config >/dev/null 2>&1; then
        log "SUCCESS" "Configuration Docker Compose valide"
    else
        log "ERROR" "Configuration Docker Compose invalide"
        docker-compose config
        exit 1
    fi
}

# Function to pull base images
pull_images() {
    log "INFO" "${GEAR} T√©l√©chargement des images de base..."
    
    base_images=(
        "postgres:15"
        "redis:7-alpine"
        "python:3.11"
        "node:18-alpine"
        "nginx:alpine"
        "grafana/grafana:latest"
    )
    
    for image in "${base_images[@]}"; do
        log "INFO" "T√©l√©chargement de $image..."
        if docker pull "$image" >/dev/null 2>&1; then
            log "SUCCESS" "Image $image t√©l√©charg√©e"
        else
            log "WARNING" "√âchec du t√©l√©chargement de $image"
        fi
    done
}

# Function to build application images
build_images() {
    log "INFO" "${GEAR} Construction des images de l'application..."
    
    if docker-compose build --parallel; then
        log "SUCCESS" "Toutes les images construites avec succ√®s"
    else
        log "ERROR" "√âchec de la construction des images"
        exit 1
    fi
}

# Function to stop existing containers
stop_existing() {
    log "INFO" "${GEAR} Arr√™t des containers existants..."
    
    if docker-compose ps -q | grep -q .; then
        docker-compose down
        log "SUCCESS" "Containers existants arr√™t√©s"
    else
        log "INFO" "Aucun container en cours d'ex√©cution"
    fi
}

# Function to start services
start_services() {
    log "INFO" "${GEAR} D√©marrage des services..."
    
    # Start database and cache first
    log "INFO" "D√©marrage des services de donn√©es..."
    docker-compose up -d postgres redis
    sleep 10
    
    # Start processing services
    log "INFO" "D√©marrage des services de traitement..."
    docker-compose up -d etl api
    sleep 10
    
    # Start web services
    log "INFO" "D√©marrage des services web..."
    docker-compose up -d frontend chatbot
    sleep 5
    
    # Start monitoring services
    log "INFO" "D√©marrage des services de monitoring..."
    docker-compose up -d grafana nginx
    sleep 5
    
    # Start remaining services
    log "INFO" "D√©marrage des services restants..."
    docker-compose up -d
    
    log "SUCCESS" "Tous les services d√©marr√©s"
}

# Function to wait for services to be healthy
wait_for_health() {
    log "INFO" "${GEAR} Attente de la stabilisation des services..."
    
    local timeout=$HEALTH_CHECK_TIMEOUT
    local elapsed=0
    local interval=5
    
    services_to_check=("postgres" "redis" "etl" "api" "frontend")
    
    while [ $elapsed -lt $timeout ]; do
        all_healthy=true
        
        for service in "${services_to_check[@]}"; do
            if ! docker-compose ps "$service" | grep -q "Up"; then
                all_healthy=false
                break
            fi
        done
        
        if [ "$all_healthy" = true ]; then
            log "SUCCESS" "Tous les services sont op√©rationnels"
            return 0
        fi
        
        log "INFO" "Attente de la stabilisation... (${elapsed}s/${timeout}s)"
        sleep $interval
        elapsed=$((elapsed + interval))
    done
    
    log "WARNING" "Timeout atteint - certains services peuvent ne pas √™tre pr√™ts"
}

# Function to run health checks
run_health_checks() {
    log "INFO" "${GEAR} Ex√©cution des v√©rifications de sant√©..."
    
    # Check if health script exists and run it
    if [ -f "scripts/health-check.sh" ]; then
        if ./scripts/health-check.sh; then
            log "SUCCESS" "Toutes les v√©rifications de sant√© pass√©es"
        else
            log "WARNING" "Certaines v√©rifications de sant√© ont √©chou√©"
        fi
    else
        # Manual basic checks
        log "INFO" "V√©rifications de base..."
        
        # Check web endpoints
        if curl -sf http://localhost:3000 >/dev/null 2>&1; then
            log "SUCCESS" "Frontend accessible"
        else
            log "WARNING" "Frontend non accessible"
        fi
        
        if curl -sf http://localhost:8787/health >/dev/null 2>&1; then
            log "SUCCESS" "API accessible"
        else
            log "WARNING" "API non accessible"
        fi
        
        if curl -sf http://localhost:8788/health >/dev/null 2>&1; then
            log "SUCCESS" "ETL accessible"
        else
            log "WARNING" "ETL non accessible"
        fi
    fi
}

