# Function to log messages
log() {
    local level=$1
    shift
    local message="$*"
    local timestamp=$(date "+%Y-%m-%d %H:%M:%S")
    
    case $level in
        "INFO")
            echo -e "${BLUE}[$timestamp] INFO:${NC} $message"
            ;;
        "SUCCESS")
            echo -e "${GREEN}[$timestamp] SUCCESS:${NC} $message"
            ;;
        "WARNING")
            echo -e "${YELLOW}[$timestamp] WARNING:${NC} $message"
            ;;
        "ERROR")
            echo -e "${RED}[$timestamp] ERROR:${NC} $message"
            ;;
    esac
}

# Function to check prerequisites
check_prerequisites() {
    log "INFO" "${GEAR} V√©rification des pr√©requis..."
    
    # Check Docker
    if ! command -v docker >/dev/null 2>&1; then
        log "ERROR" "Docker n'est pas install√©"
        exit 1
    fi
    
    # Check Docker Compose
    if ! command -v docker-compose >/dev/null 2>&1; then
        log "ERROR" "Docker Compose n'est pas install√©"
        exit 1
    fi
    
    # Check if Docker daemon is running
    if ! docker info >/dev/null 2>&1; then
        log "ERROR" "Docker daemon n'est pas en cours d'ex√©cution"
        exit 1
    fi
    
    # Check docker-compose.yml exists
    if [ ! -f "$DOCKER_COMPOSE_FILE" ]; then
        log "ERROR" "Fichier $DOCKER_COMPOSE_FILE non trouv√©"
        exit 1
    fi
    
    # Check available disk space (need at least 5GB)
    available_space=$(df . | awk 'NR==2 {print $4}')
    required_space=5242880  # 5GB in KB
    
    if [ "$available_space" -lt "$required_space" ]; then
        log "WARNING" "Espace disque insuffisant (moins de 5GB disponible)"
    fi
    
    log "SUCCESS" "Tous les pr√©requis sont satisfaits"
}

# Function to create necessary directories
setup_directories() {
    log "INFO" "${GEAR} Cr√©ation des r√©pertoires n√©cessaires..."
    
    directories=(
        "logs"
        "data/raw"
        "data/processed"
        "data/backup"
        "docker/volumes/postgres"
        "docker/volumes/redis"
        "docker/volumes/grafana"
    )
    
    for dir in "${directories[@]}"; do
        if [ ! -d "$dir" ]; then
            mkdir -p "$dir"
            log "INFO" "R√©pertoire cr√©√©: $dir"
        fi
    done
    
    log "SUCCESS" "R√©pertoires configur√©s"
}

