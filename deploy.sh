# Function to setup monitoring dashboard
setup_monitoring() {
    log "INFO" "${GEAR} Configuration du monitoring..."
    
    # Create Grafana provisioning directories
    mkdir -p docker/volumes/grafana/provisioning/{dashboards,datasources}
    
    # Create basic datasource configuration
    cat > docker/volumes/grafana/provisioning/datasources/datasources.yml << EOF
apiVersion: 1

datasources:
  - name: PostgreSQL
    type: postgres
    url: postgres:5432
    database: scrappingbot
    user: scrappingbot_user
    secureJsonData:
      password: \${POSTGRES_PASSWORD}
    jsonData:
      sslmode: disable
      maxOpenConns: 100
      maxIdleConns: 100
      maxConnLifetimeMinutes: 1440
EOF
    
    log "SUCCESS" "Monitoring configur√©"
}

# Function to cleanup on error
cleanup_on_error() {
    log "ERROR" "Erreur pendant le d√©ploiement - nettoyage..."
    docker-compose down 2>/dev/null || true
    exit 1
}

# Function to handle script interruption
handle_interrupt() {
    log "WARNING" "D√©ploiement interrompu par l'utilisateur"
    cleanup_on_error
}

# Trap signals for cleanup
