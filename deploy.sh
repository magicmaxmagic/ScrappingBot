            exit 1
            ;;
    esac
done

# Main deployment flow
main() {
    local start_time=$(date +%s)
    
    log "INFO" "${ROCKET} D√©but du d√©ploiement en mode $DEPLOYMENT_MODE"
    
    # Phase 1: Prerequisites and setup
    check_prerequisites
    setup_directories
    setup_environment
    validate_compose
    
    # Phase 2: Images
    pull_images
    build_images
    
    # Phase 3: Deployment
    stop_existing
    setup_monitoring
    start_services
    
    # Phase 4: Validation
    wait_for_health
    run_health_checks
    
    # Phase 5: Summary
    local end_time=$(date +%s)
    local duration=$((end_time - start_time))
    
    log "SUCCESS" "D√©ploiement termin√© en ${duration}s"
    show_deployment_summary
}

# Execute main function
main "$@"
